// ---------------------------------------------------------------------------
// Resource presence check
// ---------------------------------------------------------------------------

// Essential original-game files, checked through the same FileExists path the
// game uses (relative to the resource root). These are real loose files on disk
// (NOT logical/clump names the engine remaps), picked from two different
// folders so a partial copy is also caught:
//   - the frontend graphics clump (needed immediately for the main menu), and
//   - Darci's character model (present in every install).
static const char* const ESSENTIAL_FILES[] = {
    "clumps/frontend.txc",
    "data/DARCI1.all",
};

bool MISSING_RESOURCES_present(void)
{
    for (const char* f : ESSENTIAL_FILES) {
        if (!FileExists((CBYTE*)f))
            return false;
    }
    return true;
}

