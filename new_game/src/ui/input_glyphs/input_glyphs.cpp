// --- Drawing a logical prompt for the active device ------------------------

namespace {

// One device's atlas: which POLY page to draw on + its (square) pixel size for UV
// normalisation. Glyphs are addressed by raw atlas cell now, so no name table.
struct GlyphAtlas {
    SLONG poly_page;
    int atlas_px;
};

GlyphAtlas atlas_for_device(GlyphDevice dev)
{
    switch (dev) {
    case GLYPH_DEV_PS:
        return { POLY_PAGE_GLYPH_PS, INPUT_GLYPH_PS_ATLAS_PX };
    case GLYPH_DEV_XBOX:
        return { POLY_PAGE_GLYPH_GENERIC, INPUT_GLYPH_GENERIC_ATLAS_PX };
    case GLYPH_DEV_KBM:
    default:
        return { POLY_PAGE_GLYPH_KBM, INPUT_GLYPH_KEYBOARD_ATLAS_PX };
    }
}

// Map the live active_input_device to a glyph device family. DualSense ->
// PlayStation, Xbox -> generic (ABXY), everything else -> keyboard & mouse.
// (Steam Deck has no active_input_device value yet — see devlog follow-ups.)
GlyphDevice active_glyph_device()
{
    switch (active_input_device) {
    case INPUT_DEVICE_DUALSENSE:
        return GLYPH_DEV_PS;
    case INPUT_DEVICE_XBOX:
        return GLYPH_DEV_XBOX;
    case INPUT_DEVICE_KEYBOARD_MOUSE:
    default:
        return GLYPH_DEV_KBM;
    }
}

// Round to the nearest integer pixel — keeps glyph edges crisp.
float pixel_snap(float v)
{
    return (float)(SLONG)(v + 0.5f);
}

// "Clean" on-screen glyph sizes in framebuffer pixels: the source cell is 64px,
// so drawing at 64 (native), 32, 16, 8 makes the GPU sample a single mip level
// 1:1 with no trilinear blend (crisp); 128 is a clean 2x magnify. Any size in
// between blends two mip levels and softens. We pick the largest of these that
// fits the reserved box, so glyphs stay sharp instead of blurring at odd sizes.
float clean_glyph_size_px(float box_px)
{
    static const float CLEAN[] = { 128.0f, 64.0f, 32.0f, 16.0f, 8.0f };
    const int n = (int)(sizeof(CLEAN) / sizeof(CLEAN[0]));
    for (int i = 0; i < n; ++i)
        if (CLEAN[i] <= box_px)
            return CLEAN[i];
    return CLEAN[n - 1]; // box smaller than the smallest clean size
}

// Smallest clean size we still treat as "crisp enough". Below this, a clean size
// (16/8) renders the glyph at roughly half the text height — it reads as broken
// next to the letters. So when the box is this small we abandon the clean-size
// rule and resize the glyph to fill the line instead: blurry (off-mip sampling)
// but legible and matched to the text height.
#define GLYPH_CLEAN_MIN_PX 32.0f

// On-screen glyph size (real px) for a reserved box: the crisp clean size when
// that is >= GLYPH_CLEAN_MIN_PX (32+), otherwise the box itself (resize / blur).
float glyph_size_px(float box_px)
{
    const float clean = clean_glyph_size_px(box_px);
    if (clean >= GLYPH_CLEAN_MIN_PX)
        return clean;
    return pixel_snap(box_px);
}

// On-screen geometry of a glyph in virtual 640x480 coords, plus its advance.
// Computed once and shared by draw (needs the quad) and measure (needs only the
// advance), so the size/snap math can never diverge between the two.
struct GlyphLayout {
    float gx, gy, gw, gh; // quad in virtual coords
    float advance; // virtual-px advance (glyph width + inter-glyph gap)
};

// Compute the on-screen layout of a square glyph of size `line_height` (virtual
// coords) placed LEFT-aligned at x and centred vertically within the line
// height. Its footprint/advance is the glyph's own width (+ a small gap), NOT
// the full line-height box, so adjacent prompts don't get large gaps.
//
// Sizing/snapping is done in REAL framebuffer pixels (the UI scope scales
// virtual->real; doing it in virtual would lose the clean-size and pixel-snap
// benefits), then converted back to virtual.
GlyphLayout layout_glyph(float x, float y, float line_height)
{
    float sx = PolyPage::ui_scale_x();
    float sy = PolyPage::ui_scale_y();
    const float ox = PolyPage::ui_offset_x();
    const float oy = PolyPage::ui_offset_y();
    if (sx <= 0.0f)
        sx = 1.0f;
    if (sy <= 0.0f)
        sy = 1.0f;

    const float line_h_real = line_height * sy;
    const float g_real = glyph_size_px(line_h_real);

    // Snap both axes to whole framebuffer pixels so the glyph is crisp. The text
    // (FONT2D) now snaps to the same real-pixel grid, so the glyph and the letters
    // share one grid and stay aligned at every UI scale. INPUT_GLYPH_VNUDGE_PX is
    // a whole-real-pixel vertical nudge (down) to optically centre the glyph on
    // the visible text (the letter quad's geometric centre sits a bit high vs the
    // ink).
    const float gx_real = pixel_snap(x * sx + ox);
    const float gy_real = pixel_snap(y * sy + oy + (line_h_real - g_real) * 0.5f) + INPUT_GLYPH_VNUDGE_PX;

    GlyphLayout out;
    out.gx = (gx_real - ox) / sx;
    out.gy = (gy_real - oy) / sy;
    out.gw = g_real / sx;
    out.gh = g_real / sy;

    // Advance = drawn glyph width + a small inter-glyph gap, in virtual coords.
    const float gap_real = pixel_snap(g_real * INPUT_GLYPH_GAP_FRACTION);
    out.advance = (g_real + gap_real) / sx;
    return out;
}

} // namespace

// Source atlas cell size — every atlas is a uniform grid of this many px.
#define GLYPH_ATLAS_CELL_PX 64

// Draw a glyph by raw atlas CELL (col,row) from the GIVEN device's atlas. Atlases
// are uniform 64px grids; (col,row) is 0-based — row counted from the BOTTOM (the
// atlas texture is loaded V-flipped). Returns the advance width.
float input_glyph_draw_cell(GlyphDevice dev, int col, int row, float x, float y, float line_height, SWORD fade)
{
    if (col < 0 || row < 0)
        return 0.0f;
    const GlyphAtlas a = atlas_for_device(dev);

    const GlyphLayout L = layout_glyph(x, y, line_height);

    const float px = (float)a.atlas_px;
    const float cell = (float)GLYPH_ATLAS_CELL_PX;
    const float u1 = (float)col * cell / px;
    const float v1 = (float)row * cell / px;
    const float u2 = u1 + cell / px;
    const float v2 = v1 + cell / px;

    if (fade < 0)
        fade = 0;
    else if (fade > 255)
        fade = 255;
    const ULONG base_a = INPUT_GLYPH_DRAW_COLOUR >> 24;
    const ULONG faded_a = base_a * (ULONG)(255 - fade) / 255u;
    const ULONG colour = (INPUT_GLYPH_DRAW_COLOUR & 0x00FFFFFFu) | (faded_a << 24);

    PANEL_draw_quad(L.gx, L.gy, L.gx + L.gw, L.gy + L.gh, a.poly_page,
        colour, u1, v1, u2, v2);
    return L.advance;
}

// Advance width an inline glyph occupies at the given line height — same for any
// cell/device (depends only on the clean size + UI scale).
float input_glyph_cell_advance(float line_height)
{
    return layout_glyph(0.0f, 0.0f, line_height).advance;
}

// --- Rich text: word-wrapped text with inline input-prompt glyphs ----------

namespace {

// Extra vertical leading between text lines, as a fraction of the text cell
// height. ~40% keeps wrapped lines comfortably separated without looking double
// spaced.
#define INPUT_GLYPH_TEXT_LEADING_FRACTION 0.4f

// FONT2D letter vertical extent relative to the y passed to FONT2D_DrawLetter:
// the letter quad spans (y - TOP) .. (y + BOTTOM*scale/256). TOP is unscaled,
// BOTTOM scales — mirrors font2d.cpp::FONT2D_DrawLetter. Used to align an inline
// glyph's centre to the letter's VISUAL centre (not the line-box centre).
#define FONT2D_LETTER_TOP_PX 3
#define FONT2D_LETTER_BOTTOM_PX 15

// Small empirical vertical nudge (fraction of text height) on top of the
// letter-centre alignment, to centre the glyph against the visible text. With
// per-line baseline pixel-snapping the glyph was sitting low (smaller gap below
// than above), so this lifts it slightly (negative = up). Tune to taste —
// midway between the too-low (+0.04) and too-high (-0.06) trials.
#define INPUT_GLYPH_TEXT_VBIAS_FRACTION 0.00f

// Inline {token} ids resolve to glyphs via input_glyph_find() (input_prompt_map):
// the token is the glyph's id (e.g. {kb_w}, {xb_a}, {ps_cross}) and draws that
// device's cell. Unknown tokens are skipped.

// Width of one drawn text character in virtual px at `text_scale`. We add the
// +1px the font's own DrawLetter advance includes, so measured width matches
// drawn width and a greedy wrap never overflows by accumulated rounding.
float text_char_width(CBYTE ch, SLONG text_scale, bool use_alt = false)
{
    const SLONG w = use_alt ? FONT2D_GetLetterWidthAlt(ch) : FONT2D_GetLetterWidth(ch);
    return (float)(((w + 1) * text_scale) >> 8);
}

// Round a virtual-coord Y to a whole VIRTUAL pixel. FONT2D takes an integer y
// ((SLONG)), so giving each line an already-integer baseline means the letters
// and the inline glyph reference the exact same baseline; both then pixel-snap to
// the real framebuffer grid from there, staying aligned at every UI scale.
float round_virtual_y(float vy)
{
    return (float)(SLONG)(vy + 0.5f);
}

// Per-line geometry derived from the text scale, shared by every draw path so
// line spacing / inline-glyph centring can never diverge between them.
struct TextMetrics {
    float text_h; // text cell height (virtual px)
    float line_advance; // baseline-to-baseline step (cell + leading)
    float space_w; // width of a separator space
    float glyph_y_off; // y offset to centre an inline glyph on the letter ink
};

TextMetrics text_metrics(SLONG text_scale)
{
    TextMetrics m;
    m.text_h = (float)((FONT2D_LETTER_HEIGHT * text_scale) >> 8);
    m.line_advance = m.text_h + m.text_h * INPUT_GLYPH_TEXT_LEADING_FRACTION;
    m.space_w = (float)((FONT2D_GetLetterWidth(' ') * text_scale) >> 8);
    // Align an inline glyph's centre to the FONT2D letter's visual centre rather
    // than to the line-box centre (input_glyph_draw centres the glyph in the
    // text_h box we pass), then bias slightly to taste.
    const float letter_centre = (float)(-FONT2D_LETTER_TOP_PX + ((FONT2D_LETTER_BOTTOM_PX * text_scale) >> 8)) * 0.5f;
    m.glyph_y_off = letter_centre - m.text_h * 0.5f + m.text_h * INPUT_GLYPH_TEXT_VBIAS_FRACTION;
    return m;
}

// One wrapped line, as a [begin,end) range into the source string. Re-parsing
// the range with the same atom rules reproduces the exact same layout, so a line
// can be drawn on its own without re-running the wrap.
struct RichLine {
    const char* begin;
    const char* end;
};

// Lay the whole string out into wrapped lines. Records each line's char range
// into out_lines[0..max_lines) and returns the TOTAL line count (which may
// exceed max_lines — extra lines are counted but not stored). Pass out_lines =
// nullptr / max_lines = 0 for a count-only pass.
//
// The wrap is a pure function of (str, wrap_width, text_scale) and the active
// device's glyph advances, so it is deterministic — the same on every call.
int build_lines(const char* str, float wrap_width, SLONG text_scale,
    RichLine* out_lines, int max_lines, bool use_alt = false)
{
    if (!str)
        return 0;

    const TextMetrics m = text_metrics(text_scale);

    int count = 0;
    const char* line_begin = str;
    auto emit = [&](const char* b, const char* e) {
        if (out_lines && count < max_lines)
            out_lines[count] = { b, e };
        ++count;
    };

    float cursor_x = 0.0f; // absolute x cancels out; measure from 0
    bool at_line_start = true;
    bool pending_space = false;

    const char* p = str;
    while (*p) {
        if (*p == '\n') {
            emit(line_begin, p);
            ++p;
            line_begin = p;
            cursor_x = 0.0f;
            at_line_start = true;
            pending_space = false;
            continue;
        }
        if (*p == ' ') {
            pending_space = true;
            ++p;
            continue;
        }

        // Parse one atom: a {token} glyph or a run of normal characters (measure).
        const char* atom_begin = p;
        float atom_w = 0.0f;
        bool is_glyph = false;

        if (*p == '{') {
            const char* close = p + 1;
            while (*close && *close != '}')
                ++close;
            if (*close == '}') {
                const InputGlyph* g = input_glyph_find(p + 1, (unsigned long)(close - (p + 1)));
                p = close + 1;
                if (g) {
                    is_glyph = true;
                    atom_w = input_glyph_cell_advance(m.text_h);
                } else {
                    // Unknown token: skip entirely (no advance, no wrap).
                    continue;
                }
            } else {
                // Unterminated '{': treat as a normal word starting at '{'.
                while (*p && *p != ' ' && *p != '\n')
                    ++p;
                for (const char* c = atom_begin; c < p; ++c)
                    atom_w += text_char_width((CBYTE)*c, text_scale, use_alt);
            }
        } else {
            while (*p && *p != ' ' && *p != '\n' && *p != '{')
                ++p;
            for (const char* c = atom_begin; c < p; ++c)
                atom_w += text_char_width((CBYTE)*c, text_scale);
        }
        (void)is_glyph;

        const float lead = (pending_space && !at_line_start) ? m.space_w : 0.0f;

        // Wrap before placing this atom if it would overflow (never wrap at line
        // start — a single over-wide atom just overflows rather than looping).
        if (!at_line_start && cursor_x + lead + atom_w > wrap_width) {
            emit(line_begin, atom_begin);
            line_begin = atom_begin;
            cursor_x = 0.0f;
            at_line_start = true;
        }

        if (pending_space && !at_line_start)
            cursor_x += m.space_w;
        pending_space = false;
        cursor_x += atom_w;
        at_line_start = false;
    }

    emit(line_begin, p); // final line
    return count;
}

// Draw one already-laid-out line at (x, y). Walks the line's char range with the
// same atom/spacing rules build_lines used, so positions match exactly. Does NOT
// wrap (the line is known to fit).
void draw_one_line(const RichLine& line, float x, float y, SLONG text_scale,
    unsigned long colour, SWORD fade, const TextMetrics& m, SLONG font_page)
{
    float cursor_x = x;
    bool at_line_start = true;
    bool pending_space = false;

    const char* p = line.begin;
    while (p < line.end) {
        if (*p == ' ') {
            pending_space = true;
            ++p;
            continue;
        }

        const char* atom_begin = p;
        bool is_glyph = false;
        const InputGlyph* glyph = nullptr;

        if (*p == '{') {
            const char* close = p + 1;
            while (close < line.end && *close != '}')
                ++close;
            if (close < line.end && *close == '}') {
                glyph = input_glyph_find(p + 1, (unsigned long)(close - (p + 1)));
                p = close + 1;
                if (glyph) {
                    is_glyph = true;
                } else {
                    continue; // unknown token: skip
                }
            } else {
                while (p < line.end && *p != ' ')
                    ++p;
            }
        } else {
            while (p < line.end && *p != ' ' && *p != '{')
                ++p;
        }

        if (pending_space && !at_line_start)
            cursor_x += m.space_w;
        pending_space = false;

        if (is_glyph) {
            cursor_x += input_glyph_draw_cell(glyph->dev, glyph->col, glyph->row,
                cursor_x, y + m.glyph_y_off, m.text_h, fade);
        } else {
            for (const char* c = atom_begin; c < p; ++c)
                cursor_x += (float)FONT2D_DrawLetter(
                    (CBYTE)*c, (SLONG)cursor_x, (SLONG)y, colour, text_scale,
                    font_page, fade);
        }
        at_line_start = false;
    }
}

// Upper bound on wrapped lines laid out at once. Help bodies are far shorter;
// any excess is counted (for scroll bounds) but not stored/drawn.
const int RICH_TEXT_MAX_LINES = 256;

} // namespace

float input_glyph_text_draw(const char* str, float x, float y, float wrap_width,
    SLONG text_scale, unsigned long colour, SWORD fade)
{
    if (!str)
        return 0.0f;

    const TextMetrics m = text_metrics(text_scale);

    RichLine lines[RICH_TEXT_MAX_LINES];
    // Help text renders via the alt (English replacement) font — measure with its
    // matching widths so wrap/advance line up with the alt draw.
    const int total = build_lines(str, wrap_width, text_scale, lines, RICH_TEXT_MAX_LINES, /*use_alt=*/true);
    const int stored = (total < RICH_TEXT_MAX_LINES) ? total : RICH_TEXT_MAX_LINES;

    float ly = round_virtual_y(y);
    for (int i = 0; i < stored; ++i) {
        // Our help text is always English — render it through the license-clean
        // replacement atlas so it survives a localisation that overwrites the
        // game's font atlas (POLY_PAGE_FONT2D_ALT). See FONT2D_build_alt_atlas.
        draw_one_line(lines[i], x, ly, text_scale, colour, fade, m, POLY_PAGE_FONT2D_ALT);
        ly = round_virtual_y(ly + m.line_advance);
    }

    return (float)total * m.line_advance;
}

SLONG input_glyph_text_draw_scrolled(const char* str, float x, float y,
    float wrap_width, SLONG text_scale,
    unsigned long colour, SWORD fade,
    SLONG* first_line, float view_height,
    SLONG* out_fit)
{
    const TextMetrics m = text_metrics(text_scale);

    RichLine lines[RICH_TEXT_MAX_LINES];
    // Help text renders via the alt (English replacement) font — measure with its
    // matching widths so wrap/advance line up with the alt draw.
    const int total = build_lines(str, wrap_width, text_scale, lines, RICH_TEXT_MAX_LINES, /*use_alt=*/true);
    const int stored = (total < RICH_TEXT_MAX_LINES) ? total : RICH_TEXT_MAX_LINES;

    // How many WHOLE lines fit the window (floor — no partial line peeks at the
    // bottom edge, which is what lets us skip clipping entirely).
    int fit = (m.line_advance > 0.0f) ? (int)(view_height / m.line_advance) : 0;
    if (fit < 1)
        fit = 1;

    // Clamp the caller's scroll to a valid first-line and write it back.
    int first = first_line ? (int)*first_line : 0;
    int max_first = total - fit;
    if (max_first < 0)
        max_first = 0;
    if (first < 0)
        first = 0;
    if (first > max_first)
        first = max_first;
    if (first_line)
        *first_line = (SLONG)first;
    if (out_fit)
        *out_fit = (SLONG)fit;

    const int last = (first + fit < stored) ? (first + fit) : stored;
    float ly = round_virtual_y(y);
    for (int i = first; i < last; ++i) {
        // Our help text is always English — render it through the license-clean
        // replacement atlas so it survives a localisation that overwrites the
        // game's font atlas (POLY_PAGE_FONT2D_ALT). See FONT2D_build_alt_atlas.
        draw_one_line(lines[i], x, ly, text_scale, colour, fade, m, POLY_PAGE_FONT2D_ALT);
        ly = round_virtual_y(ly + m.line_advance);
    }

    return (SLONG)total;
}

// --- Input-prompt catalog (dev test page, gated by OC_DEBUG_INPUT_PROMPT_CATALOG) ---

SLONG input_prompt_catalog_draw_scrolled(float x, float y, SLONG text_scale,
    unsigned long colour, SWORD fade,
    SLONG* first_line, float view_height,
    SLONG* out_fit)
{
    const TextMetrics m = text_metrics(text_scale);
    const GlyphDevice dev = active_glyph_device();

    // Glyphs of the active device (the catalog shows the active device's set, so
    // it changes when the device changes).
    int total = 0;
    for (int i = 0; i < INPUT_GLYPH_COUNT; ++i)
        if (INPUT_GLYPHS[i].dev == dev)
            ++total;

    int fit = (m.line_advance > 0.0f) ? (int)(view_height / m.line_advance) : 0;
    if (fit < 1)
        fit = 1;

    int first = first_line ? (int)*first_line : 0;
    int max_first = total - fit;
    if (max_first < 0)
        max_first = 0;
    if (first < 0)
        first = 0;
    if (first > max_first)
        first = max_first;
    if (first_line)
        *first_line = (SLONG)first;
    if (out_fit)
        *out_fit = (SLONG)fit;

    // Draw the visible window — one row per line: "<label> - <glyph>".
    int shown = 0; // running count of active-device glyphs seen
    int drawn = 0;
    float ly = round_virtual_y(y);
    for (int i = 0; i < INPUT_GLYPH_COUNT && drawn < fit; ++i) {
        if (INPUT_GLYPHS[i].dev != dev)
            continue;
        if (shown++ < first)
            continue; // before the scroll window

        float cursor_x = x;
        for (const char* c = INPUT_GLYPHS[i].label; *c; ++c)
            cursor_x += (float)FONT2D_DrawLetter((CBYTE)*c, (SLONG)cursor_x, (SLONG)ly,
                colour, text_scale, POLY_PAGE_FONT2D, fade);
        for (const char* c = " - "; *c; ++c)
            cursor_x += (float)FONT2D_DrawLetter((CBYTE)*c, (SLONG)cursor_x, (SLONG)ly,
                colour, text_scale, POLY_PAGE_FONT2D, fade);
        input_glyph_draw_cell(dev, INPUT_GLYPHS[i].col, INPUT_GLYPHS[i].row,
            cursor_x, ly + m.glyph_y_off, m.text_h, fade);

        ly = round_virtual_y(ly + m.line_advance);
        ++drawn;
    }

    return (SLONG)total;
}
